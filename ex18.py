def cetakdua(*fungsi):
    fung1, fung2 = fungsi
    print(f"fung1: {fung1}, fung2: {fung2}")

def cetakdualagi(fung1, fung2):
    print(f"fung1: {fung1}, fung2: {fung2}")

def cetaksatu(fung1):
    print(f"fung1: {fung1}")

def cetakkosong():
    print("Tidak tercetak apapun")

cetakdua("dhira", "anka")
cetakdualagi("dhira", "anka")
cetaksatu("pertama")
cetakkosong()
